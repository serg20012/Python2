class Negative(Exception):
    def __str__(self):
        return '–û—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å! '


class Pryme:
    def __init__(self, x, y=None):
        self.x = x
        if y:
            self.y = y
        else:
            self.y = x
        if x < 0 or y < 0:
            raise Negative

    def per(self):
        p = self.x + self.x + self.y + self.y
        return p

    def s(self):
        s = self.x*self.y
        return s


c1 = Pryme(-10, 2)
print(c1.per())
print(c1.s())
