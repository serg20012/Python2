import math
from random import randint
import doctest


class Circle:
    """
    Represents a circle with a given radius.

    >>> circle1 = Circle(4)
    >>> circle1.len()
    25.132741228718345
    >>> circle1.s()
    50.26548245743669
    """

    def __init__(self, radius):
        self.radius = radius

    def len(self):
        p = 2 * math.pi * self.radius
        return p

    def s(self):
        s = math.pi * self.radius ** 2
        return s


